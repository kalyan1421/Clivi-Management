class _ResourceSplitCard extends StatelessWidget {
  final FinancialStats stats;

  const _ResourceSplitCard({required this.stats});

  @override
  Widget build(BuildContext context) {
    // Calculate total for percentage
    final total =
        stats.laborCost +
        stats.materialCost +
        stats.machineryCost +
        stats.otherCost;

    // Avoid division by zero
    final safeTotal = total > 0 ? total : 1.0;

    return Container(
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(20),
        boxShadow: [
          BoxShadow(
            color: Colors.black.withValues(alpha: 0.05),
            blurRadius: 20,
            offset: const Offset(0, 4),
          ),
        ],
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const Text(
            'Resource Split',
            style: TextStyle(fontSize: 16, fontWeight: FontWeight.bold),
          ),
          const SizedBox(height: 24),
          Row(
            children: [
              // Donut Chart
              SizedBox(
                height: 120,
                width: 120,
                child: PieChart(
                  PieChartData(
                    sectionsSpace: 0,
                    centerSpaceRadius: 40,
                    sections: [
                      PieChartSectionData(
                        color: AppColors.warning,
                        value: stats.laborCost,
                        title: '',
                        radius: 12,
                      ),
                      PieChartSectionData(
                        color: AppColors.success,
                        value: stats.materialCost,
                        title: '',
                        radius: 12,
                      ),
                      PieChartSectionData(
                        color: AppColors.primary,
                        value: stats.machineryCost,
                        title: '',
                        radius: 12,
                      ),
                    ],
                  ),
                ),
              ),
              const SizedBox(width: 24),
              // Legend
              Expanded(
                child: Column(
                  children: [
                    _LegendItem(
                      color: AppColors.warning,
                      label: 'Labor',
                      percentage: (stats.laborCost / safeTotal * 100).round(),
                    ),
                    const SizedBox(height: 12),
                    _LegendItem(
                      color: AppColors.success,
                      label: 'Material',
                      percentage: (stats.materialCost / safeTotal * 100)
                          .round(),
                    ),
                    const SizedBox(height: 12),
                    _LegendItem(
                      color: AppColors.primary,
                      label: 'Machinery',
                      percentage: (stats.machineryCost / safeTotal * 100)
                          .round(),
                    ),
                  ],
                ),
              ),
            ],
          ),
        ],
      ),
    );
  }
}

