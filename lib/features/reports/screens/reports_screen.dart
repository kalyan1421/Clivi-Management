class ReportsScreen extends ConsumerStatefulWidget {
  final String? projectId;

  const ReportsScreen({super.key, this.projectId});

  @override
  ConsumerState<ReportsScreen> createState() => _ReportsScreenState();
}

class _ReportsScreenState extends ConsumerState<ReportsScreen> {
  bool _isExporting = false;

  @override
  void initState() {
    super.initState();
    WidgetsBinding.instance.addPostFrameCallback((_) {
      ref
          .read(reportProvider.notifier)
          .loadReports(projectId: widget.projectId);
    });
  }

  Future<void> _exportPdf(ReportState state) async {
    if (_isExporting) return;
    setState(() => _isExporting = true);
    try {
      final doc = pw.Document();
      final stats = state.stats;
      final generatedAt = DateTime.now();

      doc.addPage(
        pw.MultiPage(
          pageFormat: PdfPageFormat.a4,
          margin: const pw.EdgeInsets.all(32),
          build: (pw.Context ctx) => [
            // Header
            pw.Row(
              mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
              children: [
                pw.Text(
                  'Project Insights Report',
                  style: pw.TextStyle(
                    fontSize: 22,
                    fontWeight: pw.FontWeight.bold,
                  ),
                ),
                pw.Text(
                  '${generatedAt.day}/${generatedAt.month}/${generatedAt.year}',
                  style: const pw.TextStyle(fontSize: 11, color: PdfColors.grey600),
                ),
              ],
            ),
            if (widget.projectId != null)
              pw.Text(
                'Project ID: ${widget.projectId}',
                style: const pw.TextStyle(fontSize: 10, color: PdfColors.grey500),
              ),
            pw.SizedBox(height: 16),
            pw.Divider(),
            pw.SizedBox(height: 12),

            // Financial Summary
            pw.Text(
              'Financial Summary',
              style: pw.TextStyle(fontSize: 16, fontWeight: pw.FontWeight.bold),
            ),
            pw.SizedBox(height: 8),
            _pdfRow('Total Expenses', '₹${stats.totalExpenses.toStringAsFixed(2)}'),
            _pdfRow('Labour Cost', '₹${stats.laborCost.toStringAsFixed(2)}'),
            _pdfRow('Material Cost', '₹${stats.materialCost.toStringAsFixed(2)}'),
            _pdfRow('Machinery Cost', '₹${stats.machineryCost.toStringAsFixed(2)}'),
            _pdfRow('Other Cost', '₹${stats.otherCost.toStringAsFixed(2)}'),
            pw.SizedBox(height: 16),

            // Cost Breakdown Table
            if (stats.chartData.isNotEmpty) ...[
              pw.Text(
                'Monthly Breakdown',
                style: pw.TextStyle(fontSize: 16, fontWeight: pw.FontWeight.bold),
              ),
              pw.SizedBox(height: 8),
              pw.TableHelper.fromTextArray(
                headers: ['Period', 'Amount (₹)'],
                data: stats.chartData
                    .map((d) => [d.label, d.value.toStringAsFixed(2)])
                    .toList(),
                headerStyle: pw.TextStyle(fontWeight: pw.FontWeight.bold),
                cellAlignment: pw.Alignment.centerLeft,
                headerDecoration: const pw.BoxDecoration(color: PdfColors.blueGrey100),
              ),
              pw.SizedBox(height: 16),
            ],

            // Footer
            pw.Divider(),
            pw.SizedBox(height: 8),
            pw.Text(
              'Generated by Clivi Management App · ${generatedAt.toIso8601String()}',
              style: const pw.TextStyle(fontSize: 9, color: PdfColors.grey500),
            ),
          ],
        ),
      );

      await Printing.layoutPdf(
        onLayout: (_) async => doc.save(),
        name: 'report_${widget.projectId ?? "all"}_${generatedAt.millisecondsSinceEpoch}.pdf',
      );
    } catch (e) {
      logger.e('PDF export failed: $e');
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Export failed: $e'),
            backgroundColor: AppColors.error,
          ),
        );
      }
    } finally {
      if (mounted) setState(() => _isExporting = false);
    }
  }

  pw.Widget _pdfRow(String label, String value) {
    return pw.Padding(
      padding: const pw.EdgeInsets.symmetric(vertical: 3),
      child: pw.Row(
        mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
        children: [
          pw.Text(label, style: const pw.TextStyle(fontSize: 11)),
          pw.Text(value,
              style: pw.TextStyle(
                fontSize: 11,
                fontWeight: pw.FontWeight.bold,
              )),
        ],
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    final state = ref.watch(reportProvider);
    final notifier = ref.read(reportProvider.notifier);
    final suppliersAsync = ref.watch(suppliersProvider);

    return Scaffold(
      appBar: AppBar(
        title: const Text('Insights'),
        actions: [
          if (_isExporting)
            const Padding(
              padding: EdgeInsets.symmetric(horizontal: 16),
              child: SizedBox(
                width: 20,
                height: 20,
                child: CircularProgressIndicator(strokeWidth: 2),
              ),
            )
          else
            IconButton(
              icon: const Icon(Icons.download),
              tooltip: 'Export PDF',
              onPressed: () => _exportPdf(state),
            ),
        ],
      ),
      body: RefreshIndicator(
        onRefresh: () => notifier.refresh(projectId: widget.projectId),
        child: SingleChildScrollView(
          padding: const EdgeInsets.all(16),
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              suppliersAsync.when(
                data: (suppliers) => _VendorFilter(
                  suppliers: suppliers,
                  selectedVendorId: state.selectedVendorId,
                  onChanged: (vendorId) =>
                      notifier.setVendor(vendorId, projectId: widget.projectId),
                ),
                loading: () => const Align(
                  alignment: Alignment.centerLeft,
                  child: SizedBox(
                    height: 16,
                    width: 16,
                    child: CircularProgressIndicator(strokeWidth: 2),
                  ),
                ),
                error: (error, stack) => const SizedBox.shrink(),
              ),
              const SizedBox(height: 12),

              // Time Period Tabs
              _TimeFilterTabs(
                selectedPeriod: state.selectedPeriod,
                onSelect: (period) =>
                    notifier.setPeriod(period, projectId: widget.projectId),
              ),
              const SizedBox(height: 24),

              if (state.isLoading && state.stats == FinancialStats.empty)
                const LoadingWidget(message: 'Loading insights...')
              else if (state.error != null &&
                  state.stats == FinancialStats.empty)
                AppErrorWidget(
                  message: state.error!,
                  onRetry: () => notifier.refresh(),
                )
              else ...[
                // Financial Health Card (Line Chart)
                _FinancialHealthCard(stats: state.stats),
                const SizedBox(height: 24),

                // Resource Split Card (Donut Chart)
                _ResourceSplitCard(stats: state.stats),
                const SizedBox(height: 24),

                // Vendor Analytics Section (Admin Only)
                if (widget.projectId == null) ...[
                  _VendorAnalyticsSection(),
                  const SizedBox(height: 24),
                ],

                // Operations mini-reports
                _OperationsReports(
                  materialRows: state.materialVendors,
                  machineryRows: state.machineryProjects,
                  labourRows: state.labourProjects,
                  isLoading: state.isLoading,
                ),
                const SizedBox(height: 24),

                // Insights Tip
                if (state.stats.growthPercentage != 0)
                  _InsightTip(growth: state.stats.growthPercentage),

                // Extra padding for scroll
                const SizedBox(height: 40),
              ],
            ],
          ),
        ),
      ),
    );
  }
}

class _TimeFilterTabs extends StatelessWidget {
  final TimePeriod selectedPeriod;
  final Function(TimePeriod) onSelect;

  const _TimeFilterTabs({required this.selectedPeriod, required this.onSelect});

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.all(4),
      decoration: BoxDecoration(
        color: Colors.grey[200],
        borderRadius: BorderRadius.circular(12),
      ),
      child: Row(
        children: TimePeriod.values.map((period) {
          final isSelected = selectedPeriod == period;
          return Expanded(
            child: GestureDetector(
              onTap: () => onSelect(period),
              child: Container(
                padding: const EdgeInsets.symmetric(vertical: 10),
                decoration: BoxDecoration(
                  color: isSelected ? Colors.white : Colors.transparent,
                  borderRadius: BorderRadius.circular(8),
                  boxShadow: isSelected
                      ? [
                          BoxShadow(
                            color: Colors.black.withValues(alpha: 0.1),
                            blurRadius: 4,
                            offset: const Offset(0, 2),
                          ),
                        ]
                      : null,
                ),
                alignment: Alignment.center,
                child: Text(
                  period.label,
                  style: TextStyle(
                    fontWeight: isSelected ? FontWeight.bold : FontWeight.w500,
                    color: isSelected
                        ? AppColors.primary
                        : AppColors.textSecondary,
                    fontSize: 13,
                  ),
                ),
              ),
            ),
          );
        }).toList(),
      ),
    );
  }
}

